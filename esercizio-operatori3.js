let number1 = prompt(`Dimmi un numero a tua scelta`);
let number2 = prompt(`Dimmi un altro numero a tua scelta`);

let somma = number1 + number2;
let sottrazione = number1 - number2;
let moltiplicazione = number1 * number2;
let divisione = number1 / number2;
let potenza = Math.pow(number1, number2);

console.log(`Con i numeri da te scelti, i risultati delle varie operazioni sono: somma: ${somma}, sottrazione: ${sottrazione}, moltiplicazione: ${moltiplicazione}, divisione: ${divisione}, potenza: ${potenza}`);
